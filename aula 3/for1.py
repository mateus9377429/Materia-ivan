# range(valor inicial, valor final, pulo)

for i in range(5):
    print(i, end="") #Usa-se o 'end=""' para não pular a linha

print("\n")

for i in range(1,5,1):
    print(i, end="")
print("\n")

for i in range(4, 9, 2):
    print(i, end="")
print("\n")

for i in range(5, 0, -1):
    print(i, end="")

print("\n")